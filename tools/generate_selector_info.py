"""Build-time generator: writes selector UI data as a static JS bootstrap."""

import json
import os
import sys

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

from google.protobuf import json_format
from google.protobuf.struct_pb2 import Struct

from randomizer.Enums.Types import ItemRandoFillerSelector, ItemRandoSelector, KeySelector
from randomizer.Lists.EnemyTypes import EnemySelector
from randomizer.Lists.HardMode import HardBossSelector, HardSelector
from randomizer.Lists.Item import CustomStartingMoveSelector, HHItemSelector
from randomizer.Lists.Logic import GlitchSelector, TrickSelector
from randomizer.Lists.Minigame import MinigameSelector
from randomizer.Lists.Multiselectors import BossesSelector, CBRandoSelector, FasterCheckSelector, QoLSelector, RandomColorSelector, RemovedBarrierSelector
from randomizer.Lists.Plandomizer import PlandomizerPanels, PlannableCustomLocations, PlannableItems, PlannableKroolPhases, PlannableMinigames, PlannableSpawns
from randomizer.Lists.Songs import ExcludedSongsSelector, MusicSelectionPanel, PlannableSongs, SongFilteringSelector
from randomizer.Lists.Warps import VanillaBananaportSelector
from randomizer.Lists.WrinklyHints import PointSpreadSelector
from randomizer.proto_gen import selector_info_pb2

_REPO_ROOT = os.path.join(os.path.dirname(__file__), "..")
_PB_OUT = os.path.join(_REPO_ROOT, "static", "data", "selector_info.pb")
_JS_OUT = os.path.join(_REPO_ROOT, "static", "js", "selector_info.js")

# Proto field name → Python selector list (repeated SelectorEntry fields)
ENTRY_FIELDS = [
    ("minigames", MinigameSelector),
    ("misc_changes", QoLSelector),
    ("bosses", BossesSelector),
    ("hard_mode", HardSelector),
    ("hard_bosses", HardBossSelector),
    ("enemies", EnemySelector),
    ("excluded_songs", ExcludedSongsSelector),
    ("random_colors", RandomColorSelector),
    ("song_filters", SongFilteringSelector),
    ("item_rando", ItemRandoSelector),
    ("item_filler", ItemRandoFillerSelector),
    ("keys", KeySelector),
    ("glitches", GlitchSelector),
    ("tricks", TrickSelector),
    ("helm_hurry_items", HHItemSelector),
    ("vanilla_warps", VanillaBananaportSelector),
    ("plando_items", PlannableItems),
    ("plando_minigames", PlannableMinigames),
    ("plando_phases", PlannableKroolPhases),
    ("plando_spawns", PlannableSpawns),
    ("points_spread", PointSpreadSelector),
    ("custom_starting_moves", CustomStartingMoveSelector),
    ("remove_barriers", RemovedBarrierSelector),
    ("faster_checks", FasterCheckSelector),
    ("cb_rando_levels", CBRandoSelector),
]

# Proto field name → Python dict (google.protobuf.Struct fields)
STRUCT_FIELDS = [
    ("plando_custom_locations", PlannableCustomLocations),
    ("plando_panels", PlandomizerPanels),
    ("select_song_panel", MusicSelectionPanel),
    ("select_songs", PlannableSongs),
]


def main():
    """Build proto message, write binary .pb and JS bootstrap."""
    msg = selector_info_pb2.SelectorInfo()

    for field_name, source in ENTRY_FIELDS:
        target = getattr(msg, field_name)
        for item in source:
            entry = target.add()
            entry.name = str(item.get("name", ""))
            entry.value = str(item.get("value", ""))
            entry.tooltip = str(item.get("tooltip", ""))
            if item.get("shift") is not None:
                entry.shift = int(item["shift"])
            if item.get("default") is not None:
                entry.default = int(item["default"])
            for key in ("check_count", "item_count", "num_val"):
                if key in item:
                    setattr(entry, key, int(item[key]))
            if "is_check" in item:
                entry.is_check = bool(item["is_check"])
            if item.get("tied") is not None:
                entry.tied = str(item["tied"])

    for field_name, source in STRUCT_FIELDS:
        s = Struct()
        s.update(json.loads(json.dumps(source)))
        getattr(msg, field_name).CopyFrom(s)

    serialized = msg.SerializeToString()
    os.makedirs(os.path.dirname(_PB_OUT), exist_ok=True)
    with open(_PB_OUT, "wb") as f:
        f.write(serialized)
    print(f"Wrote {_PB_OUT} ({len(serialized)} bytes)")

    d = json_format.MessageToDict(msg, preserving_proto_field_name=True, always_print_fields_with_no_presence=True)
    if "item_rando" in d:
        d["itemRando"] = d.pop("item_rando")
    js_payload = json.dumps(d, sort_keys=False, indent=2)
    os.makedirs(os.path.dirname(_JS_OUT), exist_ok=True)
    with open(_JS_OUT, "w") as f:
        f.write("// Auto-generated by tools/generate_selector_info.py — do not edit.\n")
        f.write(f"window.SELECTOR_INFO = {js_payload};\n")
    print(f"Wrote {_JS_OUT} ({len(js_payload)} chars JSON)")


if __name__ == "__main__":
    main()
